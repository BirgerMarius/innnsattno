    <h2><span class="badge badge-danger">2</span> Avtale videobesøk</h2>
    <ul>
      <li>For å kunne gjennomføre videobesøk må du allerede være klarert for ordinært besøk til innsatte. </li>

      <li>Innsatte <strong>tar selv</strong> kontakt med den besøkende han ønsker videbesøk sammen med, og avtaler et
        passende tidspunkt som passer for fengslet.</li>

      <li>Fengslet trenger <strong>din e-post adresse</strong> for å sende invitasjon til gjennomføring av besøket,
        dette kan du oppgi når innsatte ringer deg.</li>

    </ul>




    <hr class="my-5">



    <h2><span class="badge badge-danger">3</span> Bekreftelse på avtalt videobesøk</h2>
    <ul>
      <li>Når ditt videobesøk er avtalt, vil du motta en invitasjon/bekreftelse på e-post til hvilket tidspunkt
        videobesøket vil begynne. </li>
      <li>Sjekk at dato og klokkeslett stemmer.</li>
      <li>Bruk møtenummer/tilgangskode for å delta i besøksrommet. (Eksempel: 233 996 821)</li>
      <li>Du kan også klikke på "<span class="notranslate">Delta</span>" -linken.</li>
      <li>Møtenummer/tilgangskode kan være forskjellig fra gang til gang, så bruk møtenummer/tilgangskode fra den siste
        invitasjonen.</li>
    </ul>


